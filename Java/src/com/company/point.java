package com.company;

public class point {
    public int x;
    public int y;
    public point()
    {
        x = 0;
        y = 0;
    }
    public point(int a)
    {
        x = a;
        y = 0;
    }
    public point(int a, int b)
    {
        x = a;
        y = b;
    }
}
